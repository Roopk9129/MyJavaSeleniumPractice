package ListOfPrograms;

public class Swap4thProg {

	public static void main(String[] args) {
		/**
		 * input: today id friday
		 * 
		 * output: friday is today
		 */
		String str = "today id friday";
		String[] sp = str.split(" ");
		char ch = sp[1].charAt(1);
		char s='s';
		ch=s;
		System.out.println(ch);
	}
}
